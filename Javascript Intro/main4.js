// Exercise 2: Looping Through Numbers
// Use a for loop to print numbers 1 through 10 to the console.

// Steps:
// 1. Use a `for` loop with an initial value of 1, a condition that continues the loop while the value is less than or equal to 10,
//    and an increment expression that increases the value by 1 each loop iteration.
// 2. Inside the loop, use `console.log()` to print the current value of the loop counter.


for (let i = 1; i <= 10; i++) {
    console.log(i)
}